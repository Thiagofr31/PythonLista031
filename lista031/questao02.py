#Elaborar um programa que pergunte quatro valores inteiros e apresente 2 resultados:
#a) Resultado de suas adições
#b) Resultado de suas multiplicações

numero1 = int(input("Escolha um número inteiro: "))

numero2 = int(input("Escolha outro número inteiro: "))

adicao = numero1 + numero2

print("O resultado somando os dois números é: " , adicao)

multiplicacao = numero1 * numero2

print("O resultado multiplicando os dois números é: " , multiplicacao)
